<script>
    $(document).ready(function() {
        // Initialize Owl Carousel for Achievements section
      

        // Load YouTube IFrame Player API asynchronously
        var tag = document.createElement('script');
        tag.src = "https://www.youtube.com/iframe_api";
        var firstScriptTag = document.getElementsByTagName('script')[0];
        firstScriptTag.parentNode.insertBefore(tag, firstScriptTag);

        // Array to store YouTube player instances
        var players = [];
        var currentPlaying = null;

        // This function will be called when the YouTube API is ready
        window.onYouTubeIframeAPIReady = function() {
            // Initialize players for each iframe
            var iframes = document.querySelectorAll('#achievements-carousel iframe');
            iframes.forEach(function(iframe, index) {
                var player = new YT.Player(iframe, {
                    events: {
                        'onStateChange': onPlayerStateChange
                    }
                });
                players.push(player);
            });
        };

        // Handle player state changes
        function onPlayerStateChange(event) {
            if (event.data == YT.PlayerState.PLAYING) {
                // If there's a currently playing video, pause it
                if (currentPlaying && currentPlaying !== event.target) {
                    currentPlaying.pauseVideo();
                }
                // Update the current playing video
                currentPlaying = event.target;
                // Pause the carousel
                achievementsCarousel.trigger('stop.owl.autoplay');
            } else if (event.data == YT.PlayerState.PAUSED || event.data == YT.PlayerState.ENDED) {
                // Only resume the carousel if all players are not playing
                if (!players.some(player => player.getPlayerState() == YT.PlayerState.PLAYING)) {
                    achievementsCarousel.trigger('play.owl.autoplay');
                }
            }
        }
    });
</script>


